#include "libft.h"

int	ft_find_eol(char *buff)
{
	int	i;

	if (buff)
	{
		i = 0;
		while (buff[i] != '\0')
		{
			if (buff[i] == '\n')
				return (i);
			i++;
		}
	}
	return (-1);
}
